def drawpath(src,ans,scale)->None:
    ans=ans*scale
    ans=ans+((scale+1)//2)
    for i in range(0,ans.shape[0]-1,1):
        ans1=ans[i]
        ans2=ans[i+1]
        if   ans1[0]-ans2[0]==0: # 행 좌표가 같음 - 가로 방향 경로
            for j in range(ans1[1],ans2[1],1):
                src[ans1[0]][j]=RED
        elif ans1[1]-ans2[1]==0: # 열 좌표가 같음 - 세로 방향 경로
            for j in range(ans1[0],ans2[0],1):
                src[j][ans1[1]]=RED
        else:
            print("ans error")  

