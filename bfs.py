# --- 1. BFS 길찾기 함수 ---
def find_path_bfs(grid):
    """
    주어진 맵(list-of-lists)을 기반으로 BFS를 사용해 'S'에서 'G'까지의 경로를 찾습니다.
    """
    
    rows = len(grid)
    cols = len(grid[0])
    
    start = None
    goal = None

    # 1. 시작점(S)과 도착점(G) 위치 찾기
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] == 'S':
                start = (r, c)
            elif grid[r][c] == 'G':
                goal = (r, c)
        if start and goal:
            break
    
    if not start:
        return None, "시작점 'S'를 찾을 수 없습니다."
    if not goal:
        return None, "도착점 'G'를 찾을 수 없습니다."

    # 2. BFS 초기화
    queue = collections.deque([start])
    visited = {start}
    parent_map = {start: None} # 경로 역추적용

    # 3. BFS 탐색
    path_found = False
    while queue:
        current_r, current_c = queue.popleft()

        if (current_r, current_c) == goal:
            path_found = True
            break # 목표 도착

        # 4방향 탐색 (상, 하, 좌, 우)
        for dr, dc in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
            r, c = current_r + dr, current_c + dc

            # 맵 범위 체크, 방문 여부 체크
            if 0 <= r < rows and 0 <= c < cols and (r, c) not in visited:
                
                # *** 중요: 벽(1)이 아닌지 체크 ***
                if grid[r][c] != 1: 
                    visited.add((r, c))
                    queue.append((r, c))
                    parent_map[(r, c)] = (current_r, current_c)

    # 4. 경로 재구성
    if not path_found:
        return None, "경로를 찾을 수 없습니다."

    path = []
    current = goal
    while current is not None:
        path.append(current)
        current = parent_map[current]
    
    path.reverse() # S -> G 순서로 변경
    return path, "경로 찾기 성공"

# --- 2. 시각화 함수 ---

# 맵 업스케일링 함수
def upscale(src, size, scale) -> np.array:
    row = size * scale
    col = size * scale
    dst = np.zeros((row, col, 3), np.uint8)
    for i in range(0, size, 1):
        for j in range(0, size, 1):
            for k in range(0, scale, 1):
                for l in range(0, scale, 1):
                    dst[i * scale + k][j * scale + l] = src[i][j]
    return dst

# 정답열 경로 표시 함수 (cv2.line을 사용하도록 개선)
def drawpath(src_image, path_array, scale, color, thickness=3) -> None:
    """
    Numpy 경로 배열을 받아 업스케일된 이미지에 cv2.line으로 경로를 그립니다.
    path_array: [[r1, c1], [r2, c2], ...] 형태의 numpy 배열
    """
    # 각 셀의 중심 좌표로 변환
    # (r, c) * scale -> 왼쪽 위 꼭지점
    # + (scale // 2) -> 셀의 중심
    scaled_path_coords = path_array * scale + (scale // 2)
    
    # 연속된 점들을 선으로 연결
    for i in range(len(scaled_path_coords) - 1):
        # cv2.line은 (x, y) 좌표를 받으므로 (col, row) 순서로 변환
        p1 = (int(scaled_path_coords[i][1]), int(scaled_path_coords[i][0]))     # (col1, row1)
        p2 = (int(scaled_path_coords[i+1][1]), int(scaled_path_coords[i+1][0])) # (col2, row2)
        
        cv2.line(src_image, p1, p2, color, thickness)


