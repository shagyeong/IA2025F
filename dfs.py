N = len(grid)
M = len(grid[0])


def find_point(grid, target):
    for i in range(N):
        for j in range(M):
            if grid[i][j] == target:
                return (i,j)
    return None

start = find_point(grid, "S")
end = find_point(grid, "G")

visited = [[False]*M for _ in range(N)]
path = []

def dfs(x, y):
    global explored_nodes
    if (x, y) == end:
        path.append((x,y))
        return True

    visited[x][y] = True
    path.append((x,y))
    explored_nodes += 1

    for dx, dy in [(-1,0),(1,0),(0,-1),(0,1)]:
        nx, ny = x+dx, y+dy
        if 0 <= nx < N and 0 <= ny < M:
            if not visited[nx][ny] and grid[nx][ny] != 1:
                if dfs(nx, ny):
                    return True

    path.pop()
    return False

found = dfs(*start)

