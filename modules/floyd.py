def floyd_warshall_grid(grid):
    import sys
    INF = sys.maxsize
    rows = len(grid)
    cols = len(grid[0])
    
    # 좌표 → 노드 번호 매핑
    def node_number(x, y):
        return x * cols + y
    
    n = rows * cols  # 총 노드 수
    dist = [[INF]*n for _ in range(n)]
    
    # 거리 초기화
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] != 1:  # 벽이 아니면
                u = node_number(r, c)
                dist[u][u] = 0
                for dx, dy in [(1,0),(-1,0),(0,1),(0,-1)]:
                    nx, ny = r + dx, c + dy
                    if 0 <= nx < rows and 0 <= ny < cols and grid[nx][ny] != 1:
                        v = node_number(nx, ny)
                        dist[u][v] = 1  # 이동 비용 1
    
    # Floyd-Warshall
    for k in range(n):
        for i in range(n):
            for j in range(n):
                if dist[i][k] + dist[k][j] < dist[i][j]:
                    dist[i][j] = dist[i][k] + dist[k][j]
    
    # S, G 좌표 찾기
    start = goal = None
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] == 'S':
                start = node_number(r, c)
            elif grid[r][c] == 'G':
                goal = node_number(r, c)
    
    if start is None or goal is None:
        raise Exception("Start or Goal not found")
    
    # 최단 거리 반환
    distance = dist[start][goal]
    if distance == INF:
        return None  # 경로 없음
    else:
        return distance

